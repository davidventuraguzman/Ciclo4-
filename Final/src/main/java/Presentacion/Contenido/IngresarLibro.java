    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed
        String codigo=txtCodigo.getText();
        String autor=txtAutor.getText();
        String titulo=txtTitulo.getText();
        String editorial=txtEditorial.getText();
        String añoText=txtAño.getText();
        String opDoc=boxTipo.getSelectedItem().toString();
         if (codigo.isEmpty() || autor.isEmpty() || titulo.isEmpty() || editorial.isEmpty() || añoText.isEmpty()) {
        
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
        return;
    }

    // Verificar que al menos uno de los JRadioButton esté seleccionado
    if (!ratSi.isSelected() && !ratNo.isSelected()) {
        JOptionPane.showMessageDialog(null, "Seleccione al menos una opción para el estado.");
        return;
    }
     int ano=Integer.parseInt(txtAño.getText());
       
     if (opDoc=="Libro"){
            boolean estado;
            if(ratSi.isSelected()==true){
                estado=true;
            }else{
                estado=false;
            }
            Libro li=new Libro(codigo,"Libro",titulo,autor,editorial,ano,estado);
            try {

            if (!isEdition) {
                documentorepertorio.create(li);
            } else {
                documentorepertorio.update(id, li);
            }

            String successMsg = isEdition ? "modificado" : "registrado";

            javax.swing.JOptionPane.showMessageDialog(this, "Libro " + successMsg + " exitosamente.\n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            if (!isEdition) {
                limpiarCampos();

            }
        } catch (Exception e) {
            String errorMsg = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al " + errorMsg + " el libro. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }

 
        }else if (opDoc=="Revista"){
            boolean estado;
            if(ratSi.isSelected()==true){
                estado=true;
            }else{
                estado=false;
            }
            int volumen=Integer.parseInt(txtVolumen.getText());
            int numero=Integer.parseInt(txtNumero.getText());
            Revista revi=new Revista(volumen, numero, codigo,"Revista",titulo, autor, editorial, ano, estado);
            try {

            if (!isEdition) {
                documentorepertorio.create(revi);
            } else {
                documentorepertorio.update(id, revi);
            }

            String successMsg = isEdition ? "modificado" : "registrado";

            javax.swing.JOptionPane.showMessageDialog(this, "Libro " + successMsg + " exitosamente.\n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            if (!isEdition) {
               limpiarCampos();

            }
        } catch (Exception e) {
            String errorMsg = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al " + errorMsg + " el libro. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
     
            
        }
      
    }//GEN-LAST:event_IngresarActionPerformed

