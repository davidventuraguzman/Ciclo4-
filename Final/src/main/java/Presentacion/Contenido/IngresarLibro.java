    private void inicio(){
            txtAutor.setEnabled(false);
            txtAño.setEnabled(false);
            txtCodigo.setEnabled(false);
            txtEditorial.setEnabled(false);
            txtTitulo.setEnabled(false);
            txtVolumen.setEnabled(false);
            ratNo.setEnabled(false);
            ratSi.setEnabled(false);
            Ingresar.setEnabled(false);
            txtNumero.setEnabled(false);
            if (isEdition) {
            title.setText("Actulizar Libro");
            Ingresar.setText("Modificar");
            }
            if(EditarLibro!=null){
                if (EditarLibro instanceof Revista){
                Revista revi= (Revista) EditarLibro;
                txtVolumen.setText(Integer.toString(revi.getVolumen()));
                txtNumero.setText(Integer.toString(revi.getNumero()));
                txtAutor.setText(revi.getAutor());
                txtAño.setText(Integer.toString(revi.getAnoPub()));
                txtCodigo.setText(revi.getCodigo());
                txtEditorial.setText(revi.getEditorial());
                txtTitulo.setText(revi.getTitulo());
                if(revi.isPrestado()){
                    ratSi.setSelected(true);
                }else{
                    ratNo.setSelected(true);
                }
                boxTipo.setSelectedItem("Revista");
                boxTipo.setEnabled(false);
                txtCodigo.setEditable(false);
                 }else if(EditarLibro instanceof Libro){
                     Libro lib= (Libro) EditarLibro;
                txtVolumen.setEnabled(false);
                txtNumero.setEnabled(false);
                txtAutor.setText(lib.getAutor());
                txtAño.setText(Integer.toString(lib.getAnoPub()));
                txtCodigo.setText(lib.getCodigo());
                txtEditorial.setText(lib.getEditorial());
                txtTitulo.setText(lib.getTitulo());
                if(lib.isPrestado()){
                    ratSi.setSelected(true);
                }else{
                    ratNo.setSelected(true);
                    }
                boxTipo.setSelectedItem("Libro");
                boxTipo.setEnabled(false);
                txtCodigo.setEnabled(false);
                }
            }
    }
    
